// Operators
// Equals
console.log(1 == 1); //true

// Not Equals
console.log(1 != 1); //false

// smaller as
console.log(0 < 1); //true

// biger as
console.log(0 > 1); //false

// smaller or equals
console.log(0 <= 1); // true
console.log(1 <= 1); // true
console.log(2 <= 1); // false


console.log(0 >= 1); // false
console.log(1 >= 1); // true
console.log(2 >= 1); // true


// Equality
console.log(1 == "1"); //true
// Strict Equality
console.log(1 === "1"); //false
